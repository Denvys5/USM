package com.denvys5.uraniumswordmod.core;

import cofh.api.energy.EnergyStorage;
import cofh.api.energy.IEnergyStorage;
import net.minecraft.entity.player.EntityPlayer;
import net.minecraft.inventory.ISidedInventory;
import net.minecraft.item.ItemStack;
import net.minecraft.nbt.NBTTagCompound;
import net.minecraft.tileentity.TileEntity;

public abstract class TileEntityCore extends TileEntity implements ISidedInventory, IEnergyStorage{

	protected String localizedName;
	// Доступ к слотам
	protected static int[] slots_top;
	protected static int[] slots_bottom;
	protected static int[] slots_sides;
	protected ItemStack[] slots;
	protected static int resultSlot;
	protected static int fuelSlot;
	protected static int ingredSlot;
	protected int grinderSpeed;
	protected static int batteryChargeSpeed;
	protected static int powerUsage;
	protected int power;
	protected static int maxPower;
	protected int cookTime;

	@Override
	public int getSizeInventory(){
		return this.slots.length;
	}

	@Override
	public ItemStack getStackInSlot(int i){
		return this.slots[i];
	}

	@Override
	public ItemStack decrStackSize(int i, int j){
		if(this.slots[i] != null){
			ItemStack itemstack;
			if(this.slots[i].stackSize <= j){
				itemstack = this.slots[i];
				this.slots[i] = null;
				return itemstack;
			} else{
				itemstack = this.slots[i].splitStack(j);
				if(this.slots[i].stackSize == 0){
					this.slots[i] = null;
				}
				return itemstack;
			}
		}
		return null;
	}

	@Override
	public ItemStack getStackInSlotOnClosing(int i){
		if(this.slots[i] != null){
			ItemStack itemstack = this.slots[i];
			this.slots[i] = null;
			return itemstack;
		}
		return null;
	}

	@Override
	public void setInventorySlotContents(int i, ItemStack itemstack){
		this.slots[i] = itemstack;

		if(itemstack != null && itemstack.stackSize > this.getInventoryStackLimit()){
			itemstack.stackSize = this.getInventoryStackLimit();
		}
	}

	@Override
	public String getInventoryName(){
		return null;
	}

	@Override
	public boolean hasCustomInventoryName(){
		return this.localizedName != null && this.localizedName.length() > 0;
	}

	public void setGuiDisplayName(String displayName){
		this.localizedName = displayName;
	}

	@Override
	public int getInventoryStackLimit(){
		return 64;
	}

	@Override
	public boolean isUseableByPlayer(EntityPlayer entityplayer){
		return this.worldObj.getTileEntity(this.xCoord, this.yCoord, this.zCoord) != this ? false : entityplayer.getDistanceSq((double)this.xCoord + 0.5D, (double)this.yCoord + 0.5D, (double)this.zCoord + 0.5D) <= 64.0D;
	}

	@Override
	public void openInventory(){

	}

	@Override
	public void closeInventory(){

	}

	@Override
	public boolean isItemValidForSlot(int p_94041_1_, ItemStack p_94041_2_){
		return false;
	}

	@Override
	public int[] getAccessibleSlotsFromSide(int var1){
		return var1 == 0 ? slots_bottom : (var1 == 1 ? slots_top : slots_sides);
	}

	@Override
	public boolean canInsertItem(int p_102007_1_, ItemStack p_102007_2_, int p_102007_3_){
		return false;
	}

	@Override
	public boolean canExtractItem(int i, ItemStack itemstack, int j){
		if(i == this.resultSlot){
			return true;
		} else{
			return false;
		}
	}

	public boolean hasPower(){
		return this.power > this.powerUsage;
	}

	public int getRequiredPowerForCrafting(ItemStack itemstack){
		return 0;
	}

	public int operationSpeed(){
		return getRequiredPowerForCrafting(this.slots[0]) / powerUsage;
	}

	public int craftTime(ItemStack itemstack){
		return getRequiredPowerForCrafting(itemstack) / this.powerUsage;
	}

	public int getPowerRemainingScaled(int i){
		return this.power * i / this.maxPower;
	}

	public int getGrinderProgressScaled(int i){
		return this.cookTime * i / this.grinderSpeed;

	}

	// ThermalExpansion Part

	protected int energy;
	protected int capacity;
	protected int maxReceive;
	protected int maxExtract;

	/*
	 * public TileEntityCore(int capacity) {
	 * 
	 * this(capacity, capacity, capacity); }
	 * 
	 * public TileEntityCore(int capacity, int maxTransfer) {
	 * 
	 * this(capacity, maxTransfer, maxTransfer); }
	 * 
	 * public TileEntityCore(int capacity, int maxReceive, int maxExtract) {
	 * 
	 * this.capacity = capacity; this.maxReceive = maxReceive; this.maxExtract =
	 * maxExtract; }
	 */

	/*
	 * public TileEntityCore readFromNBT(NBTTagCompound nbt) {
	 * 
	 * this.energy = nbt.getInteger("Energy");
	 * 
	 * if (energy > capacity) { energy = capacity; } return this; }
	 * 
	 * public NBTTagCompound writeToNBT(NBTTagCompound nbt) {
	 * 
	 * if (energy < 0) { energy = 0; } nbt.setInteger("Energy", energy); return
	 * nbt; }
	 */

	public void setCapacity(int capacity){

		this.capacity = capacity;

		if(energy > capacity){
			energy = capacity;
		}
	}

	public void setMaxTransfer(int maxTransfer){

		setMaxReceive(maxTransfer);
		setMaxExtract(maxTransfer);
	}

	public void setMaxReceive(int maxReceive){

		this.maxReceive = maxReceive;
	}

	public void setMaxExtract(int maxExtract){

		this.maxExtract = maxExtract;
	}

	public int getMaxReceive(){

		return maxReceive;
	}

	public int getMaxExtract(){

		return maxExtract;
	}

	/**
	 * This function is included to allow for server -> client sync. Do not call
	 * this externally to the containing Tile Entity, as not all IEnergyHandlers
	 * are guaranteed to have it.
	 * 
	 * @param energy
	 */
	public void setEnergyStored(int energy){

		this.energy = energy;

		if(this.energy > capacity){
			this.energy = capacity;
		} else if(this.energy < 0){
			this.energy = 0;
		}
	}

	/**
	 * This function is included to allow the containing tile to directly and
	 * efficiently modify the energy contained in the EnergyStorage. Do not rely
	 * on this externally, as not all IEnergyHandlers are guaranteed to have it.
	 * 
	 * @param energy
	 */
	public void modifyEnergyStored(int energy){

		this.energy += energy;

		if(this.energy > capacity){
			this.energy = capacity;
		} else if(this.energy < 0){
			this.energy = 0;
		}
	}

	/* IEnergyStorage */
	@Override
	public int receiveEnergy(int maxReceive, boolean simulate){

		int energyReceived = Math.min(capacity - energy, Math.min(this.maxReceive, maxReceive));

		if(!simulate){
			energy += energyReceived;
		}
		return energyReceived;
	}

	@Override
	public int extractEnergy(int maxExtract, boolean simulate){

		int energyExtracted = Math.min(energy, Math.min(this.maxExtract, maxExtract));

		if(!simulate){
			energy -= energyExtracted;
		}
		return energyExtracted;
	}

	@Override
	public int getEnergyStored(){

		return energy;
	}

	@Override
	public int getMaxEnergyStored(){

		return capacity;
	}

}
